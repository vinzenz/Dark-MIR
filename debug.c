void Examine_objects(){
  DEBUG("------------------------------------------------------------");

  int i;
  for(i = 0; i < MAX_OBJECTS; i++){

    if(! object[i].alive) continue;
    
    fprintf(D_OUT,  " [ %4d ] D: %02X T: %02X  M: %02X \n",
       i, object[i].descriptor, object[i].type, object[i].model);

  }

  DEBUG("------------------------------------------------------------");
  return;
}
